#!/bin/sh

M0_IP=172.17.59.89
M1_IP=172.17.94.123
M2_IP=172.17.197.159
ETCD_NAME=m0
INTERNAL_IP=$M0_IP

mkdir -p /home/huang/_scripts/out
cat <<EOF | tee _scripts/out/etcd.service
[Unit]
Description=etcd
Documentation=https://github.com/coreos

[Service]
ExecStart=/usr/local/bin/etcd \\
  --name ${ETCD_NAME} \\
  --cert-file=/etc/etcd/kube-apiserver.pem \\
  --key-file=/etc/etcd/kube-apiserver-key.pem \\
  --peer-cert-file=/etc/etcd/kube-apiserver.pem \\
  --peer-key-file=/etc/etcd/kube-apiserver-key.pem \\
  --trusted-ca-file=/etc/etcd/ca.pem \\
  --peer-trusted-ca-file=/etc/etcd/ca.pem \\
  --peer-client-cert-auth \\
  --client-cert-auth \\
  --initial-advertise-peer-urls https://${INTERNAL_IP}:2380 \\
  --listen-peer-urls https://${INTERNAL_IP}:2380 \\
  --listen-client-urls https://${INTERNAL_IP}:2379,https://127.0.0.1:2379 \\
  --advertise-client-urls https://${INTERNAL_IP}:2379 \\
  --initial-cluster-token etcd-cluster-0 \\
  --initial-cluster m0=https://${M0_IP}:2380,m1=https://${M1_IP}:2380,m2=https://${M2_IP}:2380 \\
  --initial-cluster-state new \\
  --data-dir=/var/lib/etcd
Restart=on-failure
RestartSec=5

[Install]
WantedBy=multi-user.target
EOF